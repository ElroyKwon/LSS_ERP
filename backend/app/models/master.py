class UnitPrice(Base):
    __tablename__ = "unit_prices"
    id = Column(Integer, primary_key=True, index=True)
    material_id = Column(Integer, ForeignKey("materials.id"))
    vendor_id = Column(Integer, ForeignKey("companies.id"))
    price = Column(Numeric(18, 2), nullable=False)
    unit = Column(String(20))
    apply_year = Column(Integer, nullable=False)
    apply_from = Column(Date)
    apply_to = Column(Date)
    price_type = Column(String(20), default="standard")
    notes = Column(Text)
    created_by = Column(Integer, ForeignKey("users.id"))
    created_at = Column(DateTime, default=func.now())

    material = relationship("Material")
    vendor = relationship("Company")


