class Site(Base):
    __tablename__ = "sites"
    id = Column(Integer, primary_key=True, index=True)
    site_code = Column(String(20), unique=True, nullable=False)
    job_no = Column(String(20), unique=True)
    site_name = Column(String(200), nullable=False)
    client_id = Column(Integer, ForeignKey("companies.id"))
    contract_type = Column(String(20))
    site_manager_id = Column(Integer, ForeignKey("users.id"))
    start_date = Column(Date)
    end_date = Column(Date)
    status = Column(String(20), default="active")
    location = Column(Text)
    notes = Column(Text)
    created_by = Column(Integer, ForeignKey("users.id"))
    created_at = Column(DateTime, default=func.now())
    updated_at = Column(DateTime, default=func.now(), onupdate=func.now())

    client = relationship("Company", foreign_keys=[client_id])
    manager = relationship("User", foreign_keys=[site_manager_id])


class CostCode(Base):
    __tablename__ = "cost_codes"
    id = Column(Integer, primary_key=True, index=True)
    code = Column(String(30), unique=True, nullable=False)
    name = Column(String(100), nullable=False)
    parent_id = Column(Integer, ForeignKey("cost_codes.id"))
    level = Column(Integer, default=1)
    cost_type = Column(String(20))
    account_code = Column(String(20))
    is_active = Column(Boolean, default=True)
    created_at = Column(DateTime, default=func.now())

    parent = relationship("CostCode", remote_side="CostCode.id", back_populates="children")
    children = relationship("CostCode", back_populates="parent")


