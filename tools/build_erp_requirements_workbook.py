def finalize_workbook(wb):
    for ws in wb.worksheets:
        ws.sheet_properties.pageSetUpPr.fitToPage = True
        ws.page_setup.fitToWidth = 1
        ws.page_setup.fitToHeight = 0
        for row in ws.iter_rows():
            for cell in row:
                font = copy(cell.font)
                font.name = "맑은 고딕"
                cell.font = font
        if ws.max_row > 1:
            for idx in range(1, ws.max_row + 1):
                ws.row_dimensions[idx].height = 24 if idx != 1 else 28
    wb["00_요약"].sheet_properties.tabColor = "1F4E78"
    wb["01_권한매트릭스"].sheet_properties.tabColor = "C00000"
    wb["06_미결정_이슈"].sheet_properties.tabColor = "FFC000"


