def style_range(ws, min_row, max_row, min_col, max_col, fill=None, font=None):
    for row in ws.iter_rows(min_row=min_row, max_row=max_row, min_col=min_col, max_col=max_col):
        for cell in row:
            cell.border = BORDER
            cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
            if fill:
                cell.fill = fill
            if font:
                cell.font = font


def set_widths(ws, widths):
    for idx, width in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(idx)].width = width


def add_table(ws, title, headers, rows, widths, freeze="A2", filter_on=True):
    ws.append(headers)
    style_range(ws, 1, 1, 1, len(headers), HEADER_FILL, HEADER_FONT)
    for row in rows:
        ws.append(row)
    style_range(ws, 2, ws.max_row, 1, len(headers), None, NORMAL_FONT)
    set_widths(ws, widths)
    ws.freeze_panes = freeze
    if filter_on:
        ws.auto_filter.ref = f"A1:{get_column_letter(len(headers))}{ws.max_row}"
    for row in ws.iter_rows(min_row=2, max_row=ws.max_row):
        for cell in row:
            if cell.column in (1, 2, 3):
                cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
            else:
                cell.alignment = Alignment(horizontal="left", vertical="center", wrap_text=True)
    ws.sheet_view.showGridLines = False
    return ws


def add_validation(ws, col, start_row, end_row, values):
    quoted = ",".join(values)
    dv = DataValidation(type="list", formula1=f'"{quoted}"', allow_blank=True)
    ws.add_data_validation(dv)
    dv.add(f"{col}{start_row}:{col}{end_row}")


